function caseInSwitch(val) {
  var answer = '';
  switch(val) {
    case 1:
      // return 'alpha';
      answer = 'alpha';
      break;
    case 2:
      // return 'beta';
      answer = 'beta';
      break;
    case 3:
      // return 'gamma';
      answer = 'gamma';
      break;
    case 4:
      // return 'delta';
      answer = 'delta';
      break;
  }
  // Optional when return is used in cases.
  return answer;
}

caseInSwitch(1);